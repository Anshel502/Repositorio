    public void agregarvuelo() {
        String nombre = txtcodigo.getText();
        String origen = txtOrigen.getText();
        String destino = txtDestino.getText();
        String fecha = txtFecha.getText();
        String numPasajeros = txtPasajeros.getText();
    
        if (nombre.isEmpty() || origen.isEmpty() || destino.isEmpty() || fecha.isEmpty() || numPasajeros.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben ser llenados", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        try {
         int pasajeros;
            pasajeros = Integer.parseInt(numPasajeros);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese correctamente el n√∫mero de pasajeros", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        try {
            Vuelo vuelo1 = new Vuelo(nombre, origen, destino, fecha, numPasajeros);
    
            VuelosController.agregarVuelo(vuelo1);
    
            JOptionPane.showMessageDialog(this, "Vuelo agregado correctamente");
    
            this.dispose();
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Error. Por favor, use el formato  (dd/MM/yyyy).", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

      
   
       
