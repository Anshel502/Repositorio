   private void mostrarVuelosOrdenados() {
        List<Vuelo> vuelos = VuelosController.obtenerVuelos();
        vuelos.sort(Comparator.comparing(Vuelo::getFechaCompleta)); 

        StringBuilder sb = new StringBuilder("Vuelos ordenados por fecha de llegada: \n");
        for (Vuelo vuelo : vuelos) {
            sb.append(vuelo).append("\n");
        }

        txtArea.setText(sb.toString());
    }
   private void mostrarUltimoVuelo() {
        List<Vuelo> vuelos = VuelosController.obtenerVuelos();
        if (vuelos.isEmpty()) {
            txtArea.setText("No hay vuelos registrados aun.");
            return;
        }

        Vuelo ultimoVuelo = vuelos.stream()
                .max(Comparator.comparing(Vuelo::getFechaCompleta))
                .orElse(null);

        txtArea.setText("Ultimo vuelo en llegar es:\n" + (ultimoVuelo != null ? ultimoVuelo : "No hay vuelos registrados aun."));
    }
    
    private void mostrarVueloMenorPasajeros() {
        List<Vuelo> vuelos = VuelosController.obtenerVuelos();
        if (vuelos.isEmpty()) {
            txtArea.setText("No hay vuelos registrados.");
            return;
        }

        Vuelo menorPasajeros = vuelos.stream()
        .min(Comparator.comparingInt(Vuelo::getNumPasajeros))
        .orElse(null);

        txtArea.setText("Vuelo con menor numero de pasajeros:\n" + (menorPasajeros != null ? menorPasajeros : "No hay vuelos."));
    }
